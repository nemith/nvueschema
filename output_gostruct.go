// goPublic converts a kebab-case or snake_case name to a Go public identifier.
func goPublic(s string) string {
	parts := splitIdentifier(s)
	for i, p := range parts {
		if len(p) == 0 {
			continue
		}
		// Handle well-known acronyms.
		upper := strings.ToUpper(p)
		if goAcronyms[upper] {
			parts[i] = upper
		} else {
			runes := []rune(p)
			runes[0] = unicode.ToUpper(runes[0])
			parts[i] = string(runes)
		}
	}
	name := strings.Join(parts, "")
	// Prefix with X if it starts with a digit.
	if len(name) > 0 && name[0] >= '0' && name[0] <= '9' {
		name = "X" + name
	}
	return name
}

// goAcronyms is the set of well-known acronyms for Go identifier generation.
var goAcronyms = map[string]bool{
	"ID": true, "IP": true, "TCP": true, "UDP": true, "HTTP": true,
	"HTTPS": true, "URL": true, "API": true, "DNS": true, "MAC": true,
	"VLAN": true, "VRF": true, "BGP": true, "OSPF": true, "EVPN": true,
	"ACL": true, "MTU": true, "TTL": true, "QOS": true, "PTP": true,
	"SNMP": true, "SSH": true, "NTP": true, "DHCP": true, "IGMP": true,
	"MLAG": true, "NVE": true, "VNI": true, "STP": true, "LLDP": true,
}

// writeWrappedComment writes text as comment lines, wrapping at maxWidth.
func writeWrappedComment(w io.Writer, text string, prefix string, maxWidth int) {
	text = strings.TrimRight(text, "\n")
	contentWidth := max(maxWidth-len(prefix), 20)

	for paragraph := range strings.SplitSeq(text, "\n") {
		paragraph = strings.TrimSpace(paragraph)
		if paragraph == "" {
			fmt.Fprintf(w, "%s\n", strings.TrimRight(prefix, " "))
			continue
		}

		words := strings.Fields(paragraph)
		line := ""
		for _, word := range words {
			switch {
			case line == "":
				line = word
			case len(line)+1+len(word) <= contentWidth:
				line += " " + word
			default:
				fmt.Fprintf(w, "%s%s\n", prefix, line)
				line = word
			}
		}
		if line != "" {
			fmt.Fprintf(w, "%s%s\n", prefix, line)
		}
	}
}
